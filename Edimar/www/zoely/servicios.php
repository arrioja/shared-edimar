                       <td colspan="2"><p align="justify" class="style1">Las máquinas Case tienen su productividad y   resistencia certificadas por los clientes de la marca. Sin embargo, un buen   mantenimiento de los equipos hace que el rendimiento y la vida útil de repuestos   y productos sean aún más ventajosos para el usuario. Es el caso de los tacos de   las retroexcavadoras que, por funcionar como articulaciones, necesitan estar   siempre en perfecto estado para darle a la máquina el máximo de su rendimiento.   Francisco Andrade, gerente de servicios de Case compara el sistema al cuerpo   humano, donde manos, antebrazos y brazos no funcionan bien si hay algún problema   de pulso, codo u hombro. </p>
                         <p align="justify" class="style1">El  punto primordial para que el desempeño de las   retros se mantenga estable durante los trabajos es la lubricación correcta de   tacos y pernos. En condiciones normales, como en servicios de movimiento de   tierras, paisajismo o cualquier otra aplicación en ambientes secos, la   recomendación es que sea seguido el manual de instrucciones, es decir,   lubricación diaria. El ideal es que pernos y tacos tengan sus condiciones   verificadas antes que una nueva operación sea iniciada. </p>
                         <p align="justify" class="style1">La situación no es tan simple cuando el trabajo   se realiza en áreas como pantanos o en otro tipo de terreno inundado, donde, en   algunas circunstancias, las máquinas llegan a trabajar con mitad del motor   debajo del agua. En condiciones desfavorables como esas, el desgaste siempre   será mayor, pero la observación correcta del mantenimiento minimiza la   posibilidad de que ocurran daños mayores y perjuicios para el propietario de la   máquina. </p>
                         <p align="justify" class="style1">El problema resulta de la mezcla del agua,   generalmente acompañada de barro o lodo, a la grasa de los tacos. La mezcla   abrasiva ataca el equipo acelerando el desgaste. Es necesario estar alerta al   hecho de que, en estas situaciones, la simple lubricación puede no surtir   efecto. Talvez más importante sea la limpieza de los puntos donde ocurre la   lubricación. Muchas veces en los trabajos en terrenos inundados, la máquina   inmunda dificulta estas providencias, pero las mismas son fundamentales antes de   colocar una nueva camada de grasa. </p>
                         <div align="justify" class="style1">La inobservancia de las recomendaciones trae   riesgo de grandes perjuicios. Conforme cálculos de los ingenieros de Case, la   vida útil de los tacos de las retroexcavadoras es reducida a la mitad con un   mantenimiento errado. Con el desgaste, la tendencia es que las repuestos   comiencen a golpear y el ciclo operacional ideal de la máquina sea perdido. </div>
                         <p align="justify" class="style1">Lo mismo ocurre cuando el propietario retarda el   cambio de los tacos. El número de horas trabajadas que anteceden al cambio varía   de acuerdo con cada aplicación. </p>
                         <p align="justify" class="style1">La percepción de que la vida útil de las   repuestos está en el final se da cuando la cuchara llena, a pesar de haber sido   impulsada por el cilindro, demora algunos segundos para levantarse. En realidad   la máquina continuará funcionando, pero fuera de las condiciones normales, el   riesgo de problemas graves en otras partes importantes aumenta. En la lanza, con   el aumento del espacio radial entre el perno y el taco, el conjunto puede trabar   dentro del alojamiento y sólo una abertura de todo el sistema resolvería el   defecto, acarreando más gastos y días parados. </p>
                         <p class="style1"><strong>Menor desgaste con un buen operador  </strong></p>
                         <p align="justify" class="style1">La configuración adecuada del equipo, la correcta operación y   el mantenimiento preventivo son las claves para reducir el desgaste de los   elementos de contacto.</p>
                         <p align="justify" class="style1"><br />
                             <strong>Por César Sanchez* </strong></p>
                         <p align="justify" class="style1">          El contacto con el suelo inevitablemente acelera el   desgaste en ciertos componentes de los equipos, como los neumáticos o el tren de   rodaje (en las máquinas con orugas), así como también en las herramientas de   corte tales como los dientes, los cucharones, los escarificadores y las   cuchillas. </p>
                         <p align="justify" class="style1">Hay suelos que pueden ser más húmedos que otros, como también   pueden ser más rocosos, con una fisonomía más complicada o, sencillamente, más   abrasivos. No hay nada que el operador pueda hacer para cambiar eso. Sin   embargo, sí puede preparar la máquina para operar en diferentes situaciones. </p>
                         <p align="justify" class="style1">De esta manera, cuando se presentan condiciones muy exigentes,   el cargador frontal debería ser configurado con neumáticos, cucharones y dientes   para servicio pesado, y lo mismo vale para el tren de rodaje de un tractor de   orugas. En el caso de una excavadora, se podría optar por un aguilón más largo   y, en el de una zanjadora, por cadenas de configuracion diferente. Estas son   sólo algunas de las opciones disponibles para adaptar la máquina, de modo de   conseguir un mejor desempeño y con una menor vulnerabilidad al desgaste, en   diferentes aplicaciones. </p>
                         <p align="justify" class="style1">          Hay otros aspectos que influyen en el desgaste y que   sí dependen en un 100 por ciento del operador. Se trata de los factores   relacionados concretamente con el modo de operar la máquina. Aquí entran   cuestiones que van desde el arranque y calentamiento inicial del motor diesel,   hasta el apagado del mismo (en los casos con turbo), pasando por la velocidad de   trabajo, la forma de utilizar los implementos y el cuidado de no hacer patinar   excesiva e innecesariamente las ruedas, lo que no solo produce un gran desgaste   en los neumáticos sino que también genera un mayor consumo de combustible. Una   operación suave y criteriosa también aporta una parte importante al objetivo de   obtener el mejor rendimiento de los equipos, al mismo tiempo que se logra una   mayor vida útil de sus componentes. </p>
                         <p align="justify" class="style1">          Por último, debemos destacar los beneficios del   mantenimiento para prevenir el desgaste. En este sentido, el control del inflado   de los neumáticos, la tensión de las orugas, la verificación de los aprietes, el   engrase y los cambios periódicos de aceite -al igual que todas las demás   prácticas recomendadas en el manual del operador- permitirán evitar fallas   incrementando la disponibilidad de los equipos, manteniendo bajos los costos de   operación y aumentando la productividad de los mismos. </p>
                         <p align="justify" class="style1">          En síntesis, un operador competente, que conozca la   manera de operar el equipo en diferentes situaciones y que efectúe el   mantenimiento preventivo adecuado, es el mejor amigo de la máquina y de la   durabilidad de sus elementos de contacto. </p>
