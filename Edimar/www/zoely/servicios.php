                         <p align="justify" class="style1">Defectos en máquinas, sean de construcción, un coche o una   computadora, son comunes y muchas veces más frecuentes de lo que cualquiera   desearía. Pero existen formas de minimizar esos dolores de cabeza. Una de las   principales es la prevención, y es pensando en eso que Case lanzó en este   segundo semestre un nuevo servicio: el Caservice. </p>
                         <p align="justify" class="style1">El objetivo principal es garantizar al cliente siempre la mejor   atención y un equipo con el mejor desempeño. &quot;Adoptando el Caservice, el dueño   de una máquina Case será atendido siempre por personas entrenadas por la fábrica   y conocedoras de todos los detalles de los conjuntos&quot;, explica Francisco   Andrade, gerente de servicios de Case de Brasil. </p>
                         <p align="justify" class="style1">Por el contrato estándar del nuevo servicio, el cliente tiene   garantizados mantenimientos preventivos a cada 250 horas, durante dos mil horas.   Sin embargo, nada impide que dicho mantenimiento sea realizado, de acuerdo con   la voluntad del propietario de la máquina, en plazos mayores. </p>
                         <p align="justify" class="style1">Las revisiones estándar, como por ejemplo la de cien horas,   continúan valiendo con la adopción del Caservice. </p>
